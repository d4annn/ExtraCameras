package dan.extracameras.objects;

public class DoubleDuple {

    private double x;
    private double y;

    public DoubleDuple(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getY() {
        return y;
    }

    public double getX() {
        return x;
    }
}
